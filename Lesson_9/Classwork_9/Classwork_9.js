    let h1 = document.createElement("h1");
    let h2 = document.createElement("h2");
    let h3 = document.createElement("h3");

    let number = document.createElement("div");
    number.classList.add('containerNumber2');

    let newDiv = document.createElement("div");
    newDiv.classList.add('containerNumber3');
    let ul = document.createElement("ul");


    h1.innerText = element.title;
    h2.innerText = `Місяців: ${element.monthDuration}`;
    h3.innerText = `Годин: ${element.hourDuration}`;

    element.modules.forEach((moduleElement) => {
        const color1 = Math.random() * 2550;
        const color2 = Math.random() * 255;
        const color3 = Math.random() * 255;

        let li = document.createElement("li");
        li.style.background = `rgb(${color3},${color2},${color1})`
        li.innerText = moduleElement;
        ul.append(li);
    });

    number.append(h2, h3);
    div.append(h1, number, newDiv);
    document.body.append(div);
    newDiv.append(ul);

